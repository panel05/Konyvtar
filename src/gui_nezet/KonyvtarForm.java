    private void btnUjKonyvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUjKonyvActionPerformed
        String cim = txtfCim.getText();
        String szerzo1 = txtfSzerzo1.getText();
        String szerzo2 = txtfSzerzo2.getText();
        int kiadasEve = (int) spKiadasEve.getValue();
        
        if(cim.isEmpty()) lblVisszajelzesUzenet.setText("A cím nem lehet üres.");
        else if(szerzo1.isEmpty()) lblVisszajelzesUzenet.setText("Az első szerző nem lehet üres.");
        else {
            Konyv konyv = new Konyv(cim, szerzo1, szerzo2, kiadasEve);
            
            if(konyvtar.getKonyvek().isEmpty()) konyvtar.konyvHozzaadas(konyv);
            else {
                if(konyvtar.vanAzonos(konyv)) {
                    lblVisszajelzesUzenet.setText("Már van ilyen című könyv.");
                    txtfCim.setText("");
                }
                else {
                    konyvtar.konyvHozzaadas(konyv);
                    lblVisszajelzesUzenet.setText("Új könyv hozzáadva.");
                }
            }
        }
    }//GEN-LAST:event_btnUjKonyvActionPerformed

