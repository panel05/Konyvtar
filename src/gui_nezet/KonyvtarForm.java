    // Könyvtár:
    public void konyvtarKonyveinekListazasa() {
        DefaultListModel konyvtarKonyvei = new DefaultListModel();
        for(Konyv konyv : konyvtar.getKonyvek()) {
            konyvtarKonyvei.addElement(konyv.getCim());
        }
        listKonyvtarKonyvei.setModel(konyvtarKonyvei);
    }
    
    public void cmbSzerzokFeltoltese() {
        Set<String> osszesSzerzo = new HashSet<>();
        
        cmbSzerzok.removeAllItems();
        
        for(Konyv konyv : konyvtar.getKonyvek()) {
            osszesSzerzo.add(konyv.getSzerzo1());
            if(!(konyv.getSzerzo2() == null)) osszesSzerzo.add(konyv.getSzerzo2());
        }
        
        for(String szerzo : osszesSzerzo) {
            cmbSzerzok.addItem(szerzo);
        }
    }
    
    public void szerzoKonyveinekListazasa(String szerzo) {
        DefaultListModel szerzoKonyvei = new DefaultListModel();
        for(Konyv konyv : konyvtar.getKonyvek()) {
            if(konyv.getSzerzo1().equals(szerzo) || (!(konyv.getSzerzo2() == null) && konyv.getSzerzo2().equals(szerzo))) {
                szerzoKonyvei.addElement(konyv.getCim());
            }
        }
        if(listSzerzoKonyvei.getModel().getSize() == 0) {
            for(int i = 0; i < listSzerzoKonyvei.getModel().getSize(); i++) {
                listSzerzoKonyvei.remove(i);
            }
        }
        
        listSzerzoKonyvei.setModel(szerzoKonyvei);
    }
    
    public void konyvekMentese(JList<String> lista) throws FileNotFoundException, IOException {
        JFileChooser filechooser = new JFileChooser();
        int valasz = filechooser.showSaveDialog(rootPane);
        if(valasz == JFileChooser.APPROVE_OPTION) {
            ArrayList<String> konyvcimek = new ArrayList<>();
            for(int i = 0; i < lista.getModel().getSize(); i++) {
                // System.out.println(lista.getModel().getElementAt(i));
                konyvcimek.add(lista.getModel().getElementAt(i));
            }
            
            File fajl = filechooser.getSelectedFile();

            BufferedWriter bufferedwriter = new BufferedWriter(new FileWriter(fajl));
            for(String konyvcim : konyvcimek) {
                String cim;
                if(konyvcim != konyvcimek.get(konyvcimek.size() - 1)) cim = String.format("%s\n", konyvcim);
                else cim = konyvcim;
                bufferedwriter.write(cim);
            }
            bufferedwriter.close();
        }    
    }
    
